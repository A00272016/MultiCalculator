package com.example.multicalculator

class Calculator {
    //Function Addition of two numbers
    fun add(left: Int, right: Int): Int {
        return left + right
    }
    //Function for Subraction of two numbers
    fun subtract(left: Int, right: Int): Int {
        return left - right
    }
    //Function Multiplication of two numbers
    fun multiply(left: Int, right: Int): Int {
        return left * right
    }
    //Function Division of two numbers
    fun divide(left: Int, right: Int): Int {
        return left / right
    }
}
